package Percabangan;

public class SwitchCase {
    public static void main(String[] args) {
        int MenuKue=4;
        switch (MenuKue){
            case 1:
                System.out.println("Kue Cubit");
                break;
            case 2:
                System.out.println("Kue Pisang");
                break;
            case 3:
                System.out.println("kue Cucur");
                break;
            case 4:
                System.out.println("Kue Bolu");
                break;
            default:
                System.out.println("Kue Bingka");
                break;

        }
    }
}
